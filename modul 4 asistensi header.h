#include <iostream>
#include <math.h>
int tambah (float a, float b){
	return ( a + b );
	}

int kurang (float a, float b ){
	return ( a - b );
	}

int kali (float a, float b ){
	return ( a * b );
	}

int bagi (float a, float b ){
	return (a / b );
	}

int pangkat (float a, float b ){
	return pow(a , b );
	}
